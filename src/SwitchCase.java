public class SwitchCase {
    public static void main(String[] args) {
        int day=25;

        switch (day){
            case 1:
                System.out.println("Pazartesi");
                break;
            case 2:
                System.out.println("Salı");
                break;
            case 3:
                System.out.println("Çarşamba");
                break;
            default:
                System.out.println("Hafta Sonu");

        }
    }
}
